package org.copydays.rmliu;

import java.io.Writer;

public class KeyboardReader implements Reader {

    @Override
    public int read() {
        return readKeyboard();
    }

    Reader reader = new KeyboardReader();
    Writer writer = new Printer();

    public void copy() {
        int c;
        while ((c = reader.read()) != EOF) {
            writer.write();
        }
    }
}
